package 조건문;

public class If문1 {

	public static void main(String[] args) {
		int x = 0;
		
		if (x == 0) { //true
			System.out.println("내가 실행 되겠지요?");
		} else { //false 일 때 처리하고 싶은 내용을 쓰면 됌
			System.out.println("나는 조건이 맞지 않을 때 실행되는거야.");
		}
		
		if (x == 1) {
			System.out.println("나는 실행 될까요?");
		}
		

	}

}
