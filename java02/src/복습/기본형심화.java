package 복습;

public class 기본형심화 {

	public static void main(String[] args) {
		// 기본형4가지
		// 정수, 실수, 문자, 논리
		// int, double, char, boolean

		double temp = 1.2;
		int floor = 3;
		char color = 'b';
		boolean juice = false;

		String location = "선릉역";

		System.out.println("오늘 온도는 " + temp + "도");
		System.out.println("지금 층수는 " + floor + "층");
		System.out.println("내가 좋아하는 색은 " + color);
		System.out.println("오늘 주스를 먹었나? " + juice);
		System.out.println("지금 위치는 " + location);
	}

}
