package 확인문제;

public class 문제풀이3 {

	public static void main(String[] args) {
		double x = 33.3;
		double y = 22.2;

		// x가 44.2와 동일한지 프린트
		// x가 y와 동일한지 프린트
		// y가 55보다 큰지 프린트
		System.out.println("x가 44.2와 동일한가? " + (x == 44.2));
		System.out.println("x가 y와 동일한가? " + (x == y));
		System.out.println("y가 55보다 큰가? " + (y > 55));
		

	}

}
