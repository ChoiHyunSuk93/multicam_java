package 확인문제;



public class 문제풀이2 {

	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 40;
		// 두 수를 더한 값은 얼마?
		// num1에서 num2를 나누면 얼마?
		// num1에서 num2를 나눈 나머지는 얼마?
		
		int sum = num1 + num2;
		int div = num1 / num2;
		int rest = num1 % num2;
		
		System.out.println("두 수를 더한 값은 " + sum);
		System.out.println("num1에서 num2를 나누면 " + div);
		System.out.println("num1에서 num2를 나눈 나머지는 " + rest);

	}

}
